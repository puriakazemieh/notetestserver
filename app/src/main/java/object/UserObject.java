package object;

public class UserObject {

    private int id;
    private String email;
    private String fname;
    private String lname;
    private int sex;
    private String session;

    public int getId() {
        return id;
    }

    public String getEmail() {
        return email;
    }

    public String getFname() {
        return fname;
    }

    public String getLname() {
        return lname;
    }

    public int getSex() {
        return sex;
    }

    public String getSession() {
        return session;
    }
}
